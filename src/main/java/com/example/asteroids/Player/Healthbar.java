package com.example.asteroids.Player;


public class Healthbar {

    private double coordX;
    private double coordY;



    public Healthbar() {
        this.coordX = 600;
        this.coordY = 880;
    }//end of constructor





    //getters
    public double getCoordX() {return this.coordX;}

    public double getCoordY() {return this.coordY;}



}//end of Healthbar
